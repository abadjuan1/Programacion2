
package practico1;



public class Ejercicio7 {

 
    public static void main(String[] args) {
       int x = 10; // Instrucción (declaración con asignación)
        x = x + 5;  // Expresión si no se hubiera declarado antes y también instrucción al asignar
        System.out.println(x); // Instrucción

        /*
        Explicación:
        - Expresiones: generan un valor (ejemplo: x + 5).
        - Instrucciones: son acciones completas que el programa ejecuta (ejemplo: int x = 10;).
        */
    }

}
